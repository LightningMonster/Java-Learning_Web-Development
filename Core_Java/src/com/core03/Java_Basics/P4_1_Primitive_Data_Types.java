package com.core03.Java_Basics;

public class P4_1_Primitive_Data_Types {
	public static void main(String[] args) {
		
		//boolean data type
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.println("# Boolean data type: ");
		System.out.println("Is Java fun? : "+b1);
		System.out.println("Is fish tasty? : "+b2);
		
		//byte data type
		byte a = 25;
		byte t = -10;
		
		System.out.println("\n# Byte data type:");
		System.out.println("Age: "+ a);
		System.out.println("Temperature: "+ t);
		
		//short data type
		short num = 1000;
		short t1 = -200;
		
		System.out.println("\n# Short data type:");
		System.out.println("Number of Students: "+num);
		System.out.println("Temperature: " + t1);
		
		//int data type
		
		int p = 20000000;
		int d = 150000000;
		
		System.out.println("\n# int data type:");
		System.out.println("Population: " + p); 
        System.out.println("Distance: " + d);
        
        //long data type
        long w = 7800000000L;
        long l = 9460730472580800L;

        System.out.println("\n# long data type:");
        System.out.println("World Population: " + w);  
        System.out.println("Light Year Distance: " + l);
        
        //float data type
        float pi = 3.14f;
        float gravity = 9.81f;

        System.out.println("\n# float data type:");
        System.out.println("Value of Pi: " + pi);  
        System.out.println("Gravity: " + gravity);
        
        //double data type
        double pi1 = 3.141592653589793;
        double an = 6.02214076e23;

        System.out.println("\n# double data type:");
        System.out.println("Value of Pi: " + pi1);  
        System.out.println("Avogadro's Number: " + an); 
        
        //char data type
        char g = 'A';
        char s = '$';

        System.out.println("\n# Char data type:");
        System.out.println("Grade: " + g); 
        System.out.println("Symbol: " + s); 
        
        
	}
}
