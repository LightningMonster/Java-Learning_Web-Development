package src.com.core03.Java_Basics;

public class P13_Do_While_Loop {
    public static void main(String[] args) {
        System.out.println("Do while loop:");
        int count = 0;
        //Do while loop
        do{
            System.out.println("Loop number: " + count);
            count++;
        }while(count<5);
    }
}
